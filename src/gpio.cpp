/*---------------------------------------------------------------------------------------
  function : initGpio()
    @brief    : 
    @param
    @return
----------------------------------------------------------------------------------------*/
void cGpio::initGpio()
{
	// set the digital pin as output:
	pinMode(POWER_SWITCH_CO2, OUTPUT);
	digitalWrite(POWER_SWITCH_CO2, HIGH);	

	//AnalogReadResolution(12); //10, 11, 12bit
}

/*---------------------------------------------------------------------------------------
  function : setPowerCo2()
    @brief    : 
    @param
    @return
----------------------------------------------------------------------------------------*/
void cGpio::setPowerCo2(bool bOn)
{
	digitalWrite(POWER_SWITCH_CO2, bOn);
}

/*---------------------------------------------------------------------------------------
  function : readAnalog()
    @brief    : 
    @param
    @return
----------------------------------------------------------------------------------------*/
void cGpio::readAnalog(float* fA1, float* fA2)
{
#ifdef USE_RX9_CO2
	// read the value from the sensor:
	 *fA1 = analogRead(AIO_0);
	 *fA2 = analogRead(AIO_1);	
#endif	 
}



